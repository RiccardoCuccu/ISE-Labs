/**
  * @brief This function handles EXTI line[15:10] interrupts.
  */
void EXTI15_10_IRQHandler(void)
{
  /* USER CODE BEGIN EXTI15_10_IRQn 0 */
  // If we've received a valid command and waiting for the user to press a button...
  if(status == READY) {
	  raw_data = TIM1->CNT; // Extract counted value
	  uart_tx_buf[0]='T';
	  uart_tx_buf[1]=(raw_data & 0xF000) >> 12;  // Extract for each element of the buffer a 4-bit value
	  uart_tx_buf[2]=(raw_data & 0x0F00) >> 8;
	  uart_tx_buf[3]=(raw_data & 0x00F0) >> 4;
	  uart_tx_buf[4]=(raw_data & 0x000F);

	  // Convert it to ASCII representation
	  for(int i=1; i<5; i++)
		  if(uart_tx_buf[i] < 10)
			  uart_tx_buf[i] += '0'; // By adding '0' offset if it's a number [0..9]
		  else
			  uart_tx_buf[i] += ('a'-10); // By adding 'a' offset (minus 10: 10->a, 11->b ...) minus 10

	  status = SENDING;
  }
  /* USER CODE END EXTI15_10_IRQn 0 */
  HAL_GPIO_EXTI_IRQHandler(GPIO_PIN_13);
  /* USER CODE BEGIN EXTI15_10_IRQn 1 */

  /* USER CODE END EXTI15_10_IRQn 1 */
}

